package com.team34rockets.scoutingapp.models.tbaresults;

public class TeamEventStatusResult {
    private String rank;
    private String num_teams;
    private String rankingScore;

    public String getRank() {
        return rank;
    }

    public void setRank(String rank) {
        this.rank = rank;
    }

    public String getNum_teams() {
        return num_teams;
    }

    public void setNum_teams(String num_teams) {
        this.num_teams = num_teams;
    }

    public String getRankingScore() {
        return rankingScore;
    }

    public void setRankingScore(String rankingScore) {
        this.rankingScore = rankingScore;
    }
}
